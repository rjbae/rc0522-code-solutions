// Producing a boolean value to see whether or not a string is all lowercased.
// Define a function named isLowerCased with one parameter of word.
// Create a conditional if statement to see if the word is strictly equal to being
// all in lowercase by attaching the toLowerCase() method.
// Return true if it's all in lowercase.
// Return false if it's not all in lowercase.

function isLowerCased(word) {
  if (word === word.toLowerCase()) {
    return true;
  } else {
    return false;
  }
}

